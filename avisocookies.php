		<h1>Aviso de Cookies</h1>
		<h3>O que são os cookies</h3>
		<p>Um cookie é um arquivo que é baixado em seu computador ao acessar determinadas páginas da web. Os cookies permitem que uma página web, entre outras coisas, armazene e recupere informação sobre os hábitos de navegação de um utilizador ou do seu equipamento e, dependendo da informação que contenham e da forma como utiliza o seu equipamento, podem ser utilizados para reconhecer do utilizador.</p>

		<h3>Que tipos de cookies este site usa?</h3>
		<p>Este site usa os seguintes tipos de cookies:</p>

		<p><b>Cookies de análisis:</b> São aqueles que, bem tratados por nós ou por terceiros, nos permitem quantificar o número de usuários e assim realizar a medição e análise estatística do uso feito pelos usuários do serviço oferecido. Para tal, a sua navegação no nosso website é analisada de forma a melhorar a oferta de produtos ou serviços que lhe oferecemos.</p>

		<p><b>Cookies técnicas:</b> São aqueles que permitem ao usuário navegar pela área restrita e utilizar suas diversas funções, como trocar o processo de compra por um item.</p>

		<p><b>Cookies de personalização:</b> São aqueles que permitem ao usuário acessar o serviço com algumas características gerais predefinidas com base em uma série de critérios no terminal do usuário, como o idioma ou o tipo de navegador através do qual o serviço está conectado.</p>

		<p><b>Cookies de publicidade:</b> São aqueles que, bem tratados por este site ou por terceiros, permitem gerir da forma mais eficiente possível a oferta dos espaços publicitários que se encontram no site, adequando o conteúdo do anúncio ao conteúdo do serviço solicitado ou ao uso feito dele. nosso site. Para isso, podemos analisar os seus hábitos de navegação na Internet e podemos mostrar-lhe publicidade relacionada com o seu perfil de navegação.</p>

		<p><b>Cookies de publicidade comportamental:</b> São aqueles que permitem a gestão, da forma mais eficiente possível, dos espaços publicitários que, no seu caso, o editor tenha incluído numa página web, aplicação ou plataforma a partir da qual presta o serviço solicitado. Este tipo de cookie armazena informação sobre o comportamento dos visitantes obtida através da observação contínua dos seus hábitos de navegação, o que permite desenvolver um perfil específico para a apresentação de publicidade baseada no mesmo.</p>

 
